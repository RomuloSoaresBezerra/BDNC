    private void botaoVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoVerificarActionPerformed

        WKTReader reader = new WKTReader();
        Geometry geometria1 = null, geometria2 = null;
    
        try {
            geometria1 = reader.read(pesquisaGeom(campoNome1.getText(), comboTipo1.getSelectedItem().toString()));
            geometria2 = reader.read(pesquisaGeom(campoNome2.getText(), comboTipo2.getSelectedItem().toString()));
        } catch (ClassNotFoundException | SQLException | ParseException ex) {
            Logger.getLogger(TelaRelacionamentos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        switch (comboRelacionamento.getSelectedItem().toString()) {
            case "Distance":
                double dist = (geometria1.getCentroid().distance(geometria2.getCentroid()))*(40075/360);
                DecimalFormat df = new DecimalFormat("#.00");
                campoResposta.setText(""+df.format(dist)+" km");
                break;
            case "Within":
                campoResposta.setText(""+geometria1.within(geometria2));
                break;
            case "Covers":
                campoResposta.setText(""+geometria1.covers(geometria2));
                break;
            case "Touches":
                campoResposta.setText(""+geometria1.touches(geometria2));
                break;
            case "Contains":
                campoResposta.setText(""+geometria1.contains(geometria2));
                break;
            case "Crosses":
                campoResposta.setText(""+geometria1.crosses(geometria2));
                break;
            default:
                campoResposta.setText(""+geometria1.intersects(geometria2));
                break;
        }
    }//GEN-LAST:event_botaoVerificarActionPerformed

