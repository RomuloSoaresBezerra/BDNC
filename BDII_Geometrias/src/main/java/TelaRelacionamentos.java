    public String pesquisaGeom(String valor, String tipo) throws ClassNotFoundException, SQLException{
        
        Connection conn = ConFactory.getConnection();
        
        PreparedStatement stmt = conn.prepareStatement(
                "SELECT st_asEWKT(geom) from " +tipo+" where nome ilike ?");
        stmt.setString(1, valor);
        
        ResultSet rs = stmt.executeQuery();
        
        if (rs.next()) {
            String retorno = rs.getString("ST_asEWKT");
            
            stmt.close();
            conn.close();
            return retorno;
        } else {
            stmt.close();
            conn.close();
            return null;
        }
    }
    
